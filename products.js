function openPage(i) {
    const data = productdata[i];
    document.getElementById("page-back").style.display = "block";
    document.getElementById("page-img").src = `assets/products/product-${i}.png`;
    document.getElementById("page-img").alt = `desc ${i}`;

    document.getElementById("page-name").innerHTML = data.name;
    document.getElementById("page-price").innerHTML = data.price;
    document.getElementById("page-desc").innerHTML = data.desc;
    document.getElementById("page-logo").src = "assets/logos/"+data.game+".png";
}

function closePage() {
  document.getElementById("page-back").style.display = "none";
}
