const Indexes = Array.from({ length: 50 }, (_, i) => i + 1);

Indexes.forEach((i) => {
const data = productdata[i];
  const prod = document.createElement("div");
  prod.classList.add("products-cont");
  prod.classList.add(data.game);
  prod.addEventListener('click', function() {openPage(i);}, false);

  const image = document.createElement("img");
  image.src = `assets/products/product-${i}.png`;
  image.alt = "desc ${i}";
  
  const name = document.createElement("h2");
  name.innerHTML = data.name;
  
  const game = document.createElement("h4");
  game.innerHTML = data.game;
  
  const price = document.createElement("div");
  price.innerHTML = data.price;
  price.classList.add("price");

  prod.appendChild(image);
  prod.appendChild(name);
  prod.appendChild(game);
  prod.appendChild(price);
  productswrapper.appendChild(prod);
});

